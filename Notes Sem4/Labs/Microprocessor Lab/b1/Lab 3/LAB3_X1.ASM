;Program to add two 64-bit hexa numbers
DATA SEGMENT
        NUM1 DQ 4845ADB4B857E951H
        NUM2 DQ 57DF87125CD87451H
        SUM DQ ?
        CARRY DB 0
        DATA ENDS

CODE SEGMENT
        ASSUME CS:CODE DS:DATA

START:MOV AX,DATA
      MOV DS,AX

      MOV SI,0 ;OFFSET FOR NUM1
      MOV DI,8 ;OFFSET FOR NUM2
      MOV BX,16 ;OFFSET FOR SUM
      MOV CX,8  ;NO. OF ITERATIONS
      CLC ;CLEARING CARRY FLAGS

BACK:MOV AL,[SI]
     ADC AL,[DI] ;ADDING VALUES OF NUM1 AND NUM2 BYTE BY BYTE
     DAA ;DECIMAL ADJUSTMENT
     MOV [BX],AL ;STORING RESULT
     INC SI
     INC DI
     INC BX
     LOOP BACK
     ADC CARRY,0 ;STORING VALUE OF CARRY FLAG

     MOV AH,4CH
     INT 21H
     CODE ENDS
     END START
